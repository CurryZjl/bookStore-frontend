export function getBookByName(_name) {
    let result = books.filter(book =>
        book.name === _name);
    if (result[0] != null)
        return result[0];
    else
        return null;
}

export function getBookById(_id) {
    let result = books.filter(book =>
        book.id === _id);
    if (result[0] != null)
        return result[0];
    else
        return null;
}
